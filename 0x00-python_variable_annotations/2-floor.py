#!/usr/bin/env python3
"""
function to find the floor value of a float number
"""

import math


def floor(n: float) -> int:
    """
    Returns the floor value of a floating-point number.

    Args:
        n (float): The floating-point number to find the floor value of.

    Returns:
        int: The floor value of the input number.
    """
    return math.floor(n)
